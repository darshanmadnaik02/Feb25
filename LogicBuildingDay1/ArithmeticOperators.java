class ArithmeticOperators{

   public static void main(String args[]){

      int a = 125;
      int b = 24;
      System.out.print(a + " + " + b  + " = ");
      System.out.println(a + b);
      System.out.print(a + " - " + b  + " = ");
      System.out.println(a - b);
      System.out.println(a + " X " + b  + " = " + a * b );
      System.out.println(a + " / " + b  + " = " + a / b);
      System.out.println(a + " % " + b  + " = " + a % b);
     
  }
}